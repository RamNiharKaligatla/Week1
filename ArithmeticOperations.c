#include <stdio.h>
int main()
{
	int a,b;
	scanf("%d %d",&a,&b);
	printf("Sum of numbers is %d \n",a+b);
	printf("Difference between numbers is %d \n",a-b);
	printf("Product of numbers is %d \n",a*b);
	printf("First no. divided by second no. is %d \n",a/b);
	printf("First no. modulo second no. is %d \n",a%b);
	return 0;
}
